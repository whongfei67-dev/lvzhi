/**
 * 安全过滤函数
 */
export function filterUnsafeContent(input: string): { safe: boolean; filtered: string } {
  // 敏感关键词列表（简化版）
  const sensitiveKeywords = [
    '颠覆', '分裂', '暴恐', '邪教',
  ]

  let filtered = input
  let safe = true

  for (const keyword of sensitiveKeywords) {
    if (input.includes(keyword)) {
      safe = false
      filtered = input.replace(new RegExp(keyword, 'g'), '***')
    }
  }

  return { safe, filtered }
}